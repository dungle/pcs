		public object GetObjectVO(int pintID)
		{
			const string METHOD_NAME = THIS + ".GetObjectVO()";
			DataSet dstPCS = new DataSet();
			
			OleDbDataReader odrPCS = null;
			OleDbConnection oconPCS = null;
			OleDbCommand ocmdPCS = null;
			try 
			{
				string strSql = String.Empty;
				strSql=	"SELECT "
				+ STD_CostElementTypeTable.COSTELEMENTTYPEID_FLD + ","
				+ STD_CostElementTypeTable.DESCRIPTION_FLD + ","
				+ STD_CostElementTypeTable.CODE_FLD
				+ " FROM " + STD_CostElementTypeTable.TABLE_NAME
				+" WHERE " + STD_CostElementTypeTable.COSTELEMENTTYPEID_FLD + "=" + pintID;

				Utils utils = new Utils();
				oconPCS = new OleDbConnection(Utils.Instance.OleDbConnectionString);
				ocmdPCS = new OleDbCommand(strSql, oconPCS);

				ocmdPCS.Connection.Open();
				odrPCS = ocmdPCS.ExecuteReader();

				STD_CostElementTypeVO objObject = new STD_CostElementTypeVO();

				while (odrPCS.Read())
				{ 
				objObject.CostElementTypeID = int.Parse(odrPCS[STD_CostElementTypeTable.COSTELEMENTTYPEID_FLD].ToString().Trim());
				objObject.Description = odrPCS[STD_CostElementTypeTable.DESCRIPTION_FLD].ToString().Trim();
				objObject.Code = int.Parse(odrPCS[STD_CostElementTypeTable.CODE_FLD].ToString().Trim());

				}		
				return objObject;					
			}
			catch(OleDbException ex)
			{			
				throw new PCSDBException(ErrorCode.ERROR_DB, METHOD_NAME,ex);
			}			

			catch (Exception ex) 
			{
				throw new PCSDBException(ErrorCode.OTHER_ERROR, METHOD_NAME, ex);
			}
			finally 
			{
				if (oconPCS!=null) 
				{
					if (oconPCS.State != ConnectionState.Closed) 
					{
						oconPCS.Close();
					}
				}
			}
		}


