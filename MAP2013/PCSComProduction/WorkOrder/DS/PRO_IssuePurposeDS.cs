				string strSql = String.Empty;
				strSql=	"SELECT "
				+ PRO_IssuePurposeTable.ISSUEPURPOSEID_FLD + ","
				+ PRO_IssuePurposeTable.DESCRIPTION_FLD
				+ " FROM " + PRO_IssuePurposeTable.TABLE_NAME
				+" WHERE " + PRO_IssuePurposeTable.ISSUEPURPOSEID_FLD + "=" + pintID;

